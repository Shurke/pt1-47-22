"""
Задача №5
Задано время отправления поезда и время в пути до конечной станции. Требуется написать программу,
которая найдет время прибытия этого поезда (возможно, в другие сутки).
Входной файл содержит две строки. В первой строке задано время отправления, а во второй
строке – время в пути. Время отправления задается в формате «HH:MM», где HH время в часах,
которое принимает значение от 00 до 23, ММ – время в минутах, которое принимает значение
от 00 до 59. Время в пути задается двумя неотрицательными целыми числами – количество часов и
количество минут. Числа разделяются одним пробелом. Количество часов не превышает 120, минут – 59.
INPUT 	01:02       OUTPUT   05:08
        4 6
"""


print('\nЗадача №5')
t_otpr = input('Задайте время отправления поезда в формате «HH:MM»: ').split(':')
t_put = input('Задайте  время поезда в пути, число часов и минут разделив пробелами: ').split()
t_puth = int(t_otpr[0]) + int(t_put[0]) % 24
t_putmin = int(t_otpr[1]) + int(t_put[1])
if t_putmin > 59:
    t_putmin = t_putmin - 60
    t_puth += 1
if t_puth > 23:
    t_puth = t_puth - 24
if t_puth < 10 and t_putmin < 10:
    print(f'Время прибытия 0{t_puth}:0{t_putmin}')
elif t_puth < 10 and t_putmin > 9:
    print(f'Время прибытия 0{t_puth}:{t_putmin}')
elif t_puth > 9 and t_putmin < 10:
    print(f'Время прибытия {t_puth}:0{t_putmin}')
else:
    print(f'Время прибытия {t_puth}:{t_putmin}')
