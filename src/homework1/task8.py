"""
Задача №1
На стандартной шахматной доске 8х8 заданы координаты двух клеток.
Требуется определить: имеют ли данные клетки одинаковый цвет?
Входной файл содержит целые числа x1, y1, x2, y2, описывающие координаты двух клеток (x1,y1) и
(x2,y2). Ограничения: 1 ≤ x1,y1,x2,y2 ≤ 8.
В выходной файл  выведите YES, если поля одного цвета, или слово NO в противном случае.
input 1 1 4 4    output  YES
input 1 2 7 5    output  NO
"""


print('Задача №1')
xy_1 = input('Введите координаты первой клетки  x1 и y1: ')
xy_2 = input('Введите координаты второй клетки  x2 и y2: ')
xy_11 = [int(xy) for xy in xy_1.split() if xy.isdigit()]
xy_22 = [int(yx) for yx in xy_2.split() if yx.isdigit()]
if (xy_11[0] + xy_11[1]) % 2 != (xy_22[0] + xy_22[1]) % 2:
    print('NO')
else:
    print('YES')


"""
Задача №2
Напишите программу  для создания двух строк из заданной строки.
Создайте первую строку, используя те символы, которые встречаются только один раз, и создайте
вторую строку, состоящую из многократно встречающихся символов в указанной строке.
"""


print('\nЗадача №2')
str_in = list(input('Введите строку: '))
str_orig = []
str_rep = []
while len(str_in) != 0:
    symbol = str_in.pop(0)
    if symbol not in str_in:
        str_orig.append(symbol)
    else:
        str_rep.append(symbol)
        while symbol in str_in:
            str_in.remove(symbol)
print(f'Строка из уникальных символов: {"".join(str_orig)}')
print(f'Строка из повторяющихся символов: {"".join(str_rep)}')


