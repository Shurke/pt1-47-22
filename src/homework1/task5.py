"""
Выведите n-ое число Фибоначчи, используя только временные переменные,
циклические операторы и условные операторы. n - вводится
"""


def get_fibnum(num):
    """Вычисляет N число в последовательности фибоначчи

    :param int num: номер числа в последовательности Фибоначчи
    :returns: Значения введенного числа в последовательности Фибоначчи
    :rtype: int
    """
    step = 1
    a, b = 0, 1

    while step < num:
        a, b = b, a + b
        step += 1
    return a


input_int = int(input("Введите искомый номер числа в последовательности Фибоначчи: "))
print(get_fibnum(input_int))
